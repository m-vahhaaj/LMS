    private void generateStudentID() {
        try {
            
            pst = con.prepareStatement("SELECT max(book_id) FROM book;");
            rs = pst.executeQuery();
            if(rs.next()) {
                int x = rs.getInt(1);
                studentID = ++x;
            }
            else {
                studentID = 1;
            }
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        jTextField1.setText(""+studentID);
    }

