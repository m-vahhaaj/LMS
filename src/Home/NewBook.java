    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(isFormIncomplete()) {
            JOptionPane.showMessageDialog(null, "Fill all boxes!");
            return;
        }
        String name = jTextField2.getText();
        int edition = Integer.parseInt(jTextField3.getText());
        String publisher = jTextField4.getText();
        int price = Integer.parseInt(jTextField5.getText());
        int pages = Integer.parseInt(jTextField6.getText());
        String query = "INSERT INTO book(name,edition,publisher,price,pages) VALUES"
                + "(?,?,?,?,?);";
        try {
            
            pst = con.prepareStatement(query);
            pst.setString(1, name);
            pst.setInt(2, edition);
            pst.setString(3, publisher);
            pst.setInt(4, price);
            pst.setInt(5, pages);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Book added to Database!");
            generateBookID();
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            JOptionPane.showMessageDialog(null, "Book could not be added to Database!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed



