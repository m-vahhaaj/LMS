    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jTextField7.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter Student ID!");
            return;
        }
        try {
            
            String query = "SELECT name,f_name,course,branch,year,semester FROM student WHERE s_id = ?;";
            pst = con.prepareStatement(query);
            pst.setInt(1, Integer.parseInt(jTextField7.getText()));
            rs = pst.executeQuery();
            if(rs.next()) {
                jTextField8.setText(rs.getString(1));
                jTextField9.setText(rs.getString(2));
                jTextField10.setText(rs.getString(3));
                jTextField11.setText(rs.getString(4));
                jTextField12.setText(new SimpleDateFormat("YYYY-MM-DD").format(rs.getDate(5)));
                jTextField13.setText(rs.getString(6));
                studentFound = true;
            }
            else {
                JOptionPane.showMessageDialog(null, "No student found!");
                studentFound = false;
            }
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

