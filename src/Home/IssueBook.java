    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(!bookFound || !studentFound) {
            JOptionPane.showMessageDialog(null, "Incomplete or incorrrect data!");
            return;
        }
        
        int b_id = Integer.parseInt(jTextField1.getText());
        int s_id = Integer.parseInt(jTextField7.getText());
        String query = "INSERT INTO issued_books VALUES(?,?,?,?);";
        
        try {
            
            pst = con.prepareStatement(query);
            pst.setInt(1, b_id);
            pst.setInt(2, s_id);
            pst.setDate(3, java.sql.Date.valueOf(date));
            pst.setInt(4, acc_id);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Book issued successfully!");
            
        } catch(MySQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(null, "The Book is already issued check Statistics!");
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
            JOptionPane.showMessageDialog(null, "Book not issued!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed



