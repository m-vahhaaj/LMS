    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(isFormIncomplete()) {
            JOptionPane.showMessageDialog(null, "Fill all boxes!");
            return;
        }
        else if(jTextField5.getText().length() > 4 || jTextField5.getText().length() < 4) {
            JOptionPane.showMessageDialog(null, "Enter year as: [YYYY]");
            return;
        }
        else if(jTextField6.getText().length() > 2 || jTextField6.getText().length() < 2) {
            JOptionPane.showMessageDialog(null, "Enter month as: [MM]");
            return;
        }
        else if(Integer.parseInt(jTextField6.getText()) > 12 || Integer.parseInt(jTextField6.getText()) < 1) {
            JOptionPane.showMessageDialog(null, "Enter month in range 1-12");
            return;
        }
        else if(jTextField7.getText().length() > 2 || jTextField7.getText().length() < 2) {
            JOptionPane.showMessageDialog(null, "Enter day as: [DD]");
            return;
        }
        else if(Integer.parseInt(jTextField7.getText()) > 31 || Integer.parseInt(jTextField7.getText()) < 0) {
            JOptionPane.showMessageDialog(null, "Enter day in range 1-31");
            return;
        }
        
        String name = jTextField2.getText();
        String f_name = jTextField3.getText();
        String course = (String)jComboBox1.getSelectedItem();
        String branch = jTextField4.getText();
        String year = jTextField5.getText()+"-"+jTextField6.getText()+"-"+jTextField7.getText();
        int semester = Integer.parseInt((String)jComboBox2.getSelectedItem());
        String query = "INSERT INTO student(name,f_name,course,branch,year,semester) VALUES"
                + "(?,?,?,?,?,?);";
        
        try {
            
            pst = con.prepareStatement(query);
            pst.setString(1, name);
            pst.setString(2, f_name);
            pst.setString(3, course);
            pst.setString(4, branch);
            pst.setDate(5, java.sql.Date.valueOf(year));
            pst.setInt(6, semester);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Student added to Database!");
            generateStudentID();
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
            JOptionPane.showMessageDialog(null, "Book could not be added to Database!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed



